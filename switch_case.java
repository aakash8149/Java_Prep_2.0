package Conditional_Statements;

public class switch_case {
    public static void main(String[] args) {
        int number = 2; // We should take input here !

        switch (number) {
            case 1: System.out.println("Samosa Laoo");
                break;
            case 2: System.out.println("Pakoda Laaoo");
                break;
            case 3: System.out.println("Burger Laaooo");
                break;
            default: System.out.println("No Order !!");
                break;
        }
    }
}
